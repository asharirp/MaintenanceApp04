package com.example.mtc_app_04;

public class EquipmentEntry2HelperClass {

    String eqName, eqNo;

    public EquipmentEntry2HelperClass(String eqName, String eqNo) {
        this.eqName = eqName;
        this.eqNo = eqNo;
    }

    public String getEqName() {
        return eqName;
    }

    public void setEqName(String eqName) {
        this.eqName = eqName;
    }

    public String getEqNo() {
        return eqNo;
    }

    public void setEqNo(String eqNo) {
        this.eqNo = eqNo;
    }
}
